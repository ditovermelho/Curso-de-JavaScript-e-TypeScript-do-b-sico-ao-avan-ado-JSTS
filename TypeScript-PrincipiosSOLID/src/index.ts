/*
DRY (Don’t Repeat Yourself):
- Vantagens:
Reduz a duplicação de código, melhora a manutenibilidade e evita inconsistências.

- Desvantagens:
Pode levar a abstrações excessivas se aplicado de forma rígida.

YAGNI (You Ain’t Gonna Need It):
- Vantagens:
Evita desenvolver funcionalidades não essenciais, mantendo o foco no necessário.

- Desvantagens:
Pode resultar em falta de flexibilidade se aplicado de forma excessiva.

KISS (Keep It Simple, Stupid):
- Vantagens:
Promove simplicidade, facilitando a compreensão e manutenção do código.

- Desvantagens:
Pode ser mal interpretado como “simples demais”.

SOC (Separation of Concerns):
- Vantagens:
Melhora a modularidade e facilita a manutenção, separando diferentes responsabilidades.

- Desvantagens:
Pode aumentar a complexidade se não aplicado corretamente.

MVP (Minimum Viable Product):
- Vantagens:
Lança produtos mais rapidamente, focando no essencial.

- Desvantagens:
Pode não atender a todas as expectativas do usuário inicialmente.
*/
